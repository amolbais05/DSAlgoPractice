public class Primitives {
    public static void main(String[] args)
    {
        // Primitive
        int rollNo = 64; // 4 bytes
        char letter = 'A'; // 2 bytes
        float marks = 98.67f; // 4 bytes
        double largeDecimalNumber = 4566644.4566; // 8 bytes
        long largeIntegerValue = 132243242342424L; // 8 bytes
        boolean check = false; // 1 bit

        // Non primitive
        String name = "Krishna";
    }
}
